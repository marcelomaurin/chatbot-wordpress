<h3>Bem vindo ao controle de chatbot da Maurinsoft.</h3>
<p>O objetivo deste projeto é criar um chatbot para entendimento.</p>
<p>Existem várias abas neste plugin, cada qual com sua responsábilidade.</p>
<p><b>Teste do Chatbot</b> - Permite realizar comunicação com o chatbot, simulando o que foi aprendido.</p>
<p><b>Base Perguntas</b> - Permite criar uma base de perguntas que serão associadas a sua respectiva resposta, será utilizado na base de treinamento.</p>
<p><b>Histórico</b> - Mostra tudo que foi perguntado.</p>
<p><b>Base de Respostas</b> - Permite cria uma base de respostas.</p>
<p><b>Associação P&R</b> - Permite realizar a associação entre perguntas e suas respostas.</p>
<p><b>Base de Operações</b> - Cria as ações de treinamento da base.</p>


